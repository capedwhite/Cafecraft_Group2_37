public Orderhistorypanelreuse addhistory(Orderjoin order){
    Orderhistorypanelreuse card = new Orderhistorypanelreuse();
    card.getUsername().setText(order.getUsername());
    card.getorderid().setText(String.valueOf(order.getOrderId()));
    card.getstatus().setText(order.getstatus());
    List<Orderentry>orders = order.getOrderDetails();
    String[][] data = new String[orders.size()][3];

    for (int i = 0; i < orders.size(); i++) {
        Orderentry entry = orders.get(i);
        
        data[i][0] = entry.getProductName();
        data[i][1] = String.valueOf(entry.getQty());
        data[i][2] = "Rs. " + entry.getUnitPrice();
    }

   String[] cols = {"Item name", "Quantity", "Price"};
    DefaultTableModel model = new DefaultTableModel(data, cols);
    card.getordertable().setModel(model);
    orderdisplaypanel.add(card);
    return card;
    
}
